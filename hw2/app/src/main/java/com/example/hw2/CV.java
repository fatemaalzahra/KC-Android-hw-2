package com.example.hw2;

public class CV {
 private String cv;
 private  String cvname;
 private  int cvage;
 private String cvjob;
 private int cvphonenumber;
 private String cvemail;

    public String getCv() {
        return cv;
    }

    public void setCv(String cv) {
        this.cv = cv;
    }

    public int getCvname() {
        return cvname;
    }

    public void setCvname(int cvname) {
        this.cvname = cvname;
    }

    public int getCvage() {
        return cvage;
    }

    public void setCvage(int cvage) {
        this.cvage = cvage;
    }

    public int getCvjob() {
        return cvjob;
    }

    public void setCvjob(int cvjob) {
        this.cvjob = cvjob;
    }

    public int getCvphonenumber() {
        return cvphonenumber;
    }

    public void setCvphonenumber(int cvphonenumber) {
        this.cvphonenumber = cvphonenumber;
    }

    public int getCvemail() {
        return cvemail;
    }

    public void setCvemail(int cvemail) {
        this.cvemail = cvemail;
    }
}
